#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const GENERATED_ROOT = join(ROOT, "docs", "website", "api", "reference");

const packages = [
  {
    id: "engine",
    name: "@gis-engine/engine",
    entryPoint: "packages/engine/src/index.ts",
    tsconfig: "packages/engine/tsconfig.json",
    outDir: join(GENERATED_ROOT, "engine"),
    intro:
      "Auto-generated API reference for the core runtime. Use this section as the canonical symbol-level reference for MapSpec validation, commands, runtime APIs, diagnostics, and PMTiles/source-readiness helpers.",
  },
  {
    id: "ai",
    name: "@gis-engine/ai",
    entryPoint: "packages/ai/src/index.ts",
    tsconfig: "packages/ai/tsconfig.json",
    outDir: join(GENERATED_ROOT, "ai"),
    intro:
      "Auto-generated API reference for the AI and MCP package. Use this section for MCP server exports, generation evidence types, and AI-side helper functions.",
  },
  {
    id: "cli",
    name: "@gis-engine/cli",
    entryPoint: "packages/cli/src/index.ts",
    tsconfig: "packages/cli/tsconfig.json",
    outDir: join(GENERATED_ROOT, "cli"),
    intro:
      "Auto-generated API reference for the CLI package. Use this section for the programmatic CLI surface, preflight helpers, artifact verification helpers, and provider utilities.",
  },
];

function runTypedoc(pkg) {
  rmSync(pkg.outDir, { recursive: true, force: true });
  mkdirSync(pkg.outDir, { recursive: true });

  const args = [
    "exec",
    "typedoc",
    "--plugin",
    "typedoc-plugin-markdown",
    "--tsconfig",
    pkg.tsconfig,
    "--entryPoints",
    pkg.entryPoint,
    "--out",
    pkg.outDir,
    "--readme",
    "none",
    "--entryFileName",
    "index",
    "--hideGenerator",
    "--hideBreadcrumbs",
    "--parametersFormat",
    "table",
    "--disableSources",
    "--excludePrivate",
    "--excludeProtected",
    "--excludeInternal",
    "--sort",
    "source-order",
    "--includeVersion",
  ];

  execFileSync("pnpm", args, {
    cwd: ROOT,
    stdio: "inherit",
  });
}

function patchIndex(pkg) {
  const indexPath = join(pkg.outDir, "index.md");
  const original = readFileSync(indexPath, "utf8")
    .replace(/^\*\*.+?\*\*\n\n\*\*\*\n\n# .+\n\n/s, "")
    .trimStart();
  const patched = [
    "---",
    `title: "${pkg.name} API Reference"`,
    "---",
    "",
    `# ${pkg.name} API Reference`,
    "",
    "> This page is generated by `pnpm docs:api`. Edit code comments or the API landing pages instead of editing this file by hand.",
    "",
    pkg.intro,
    "",
    original,
  ].join("\n");
  writeFileSync(indexPath, `${patched.trimEnd()}\n`, "utf8");
}

function sanitizeMarkdownFiles(dir) {
  const entries = readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) {
      sanitizeMarkdownFiles(path);
      continue;
    }
    if (!entry.isFile() || !entry.name.endsWith(".md")) continue;
    const text = readFileSync(path, "utf8");
    const sanitized = text.replace(/<([^\s<>]+)>/g, "&lt;$1&gt;");
    if (sanitized !== text) {
      writeFileSync(path, sanitized, "utf8");
    }
  }
}

for (const pkg of packages) {
  console.log(`Generating API reference for ${pkg.name}...`);
  runTypedoc(pkg);
  patchIndex(pkg);
  sanitizeMarkdownFiles(pkg.outDir);
}

console.log("API reference generation complete.");
